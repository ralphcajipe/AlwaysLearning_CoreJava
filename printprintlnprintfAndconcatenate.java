public class printprintlnprintfAndconcatenate {
    public static void main(String[] args) {
        String msg = "World";
        System.out.print("Hi");
        System.out.println("Ralph");
        System.out.printf("%s", msg);
        System.out.println("Msg: "+msg);

        /**
         * Output:
         * HiRalph
         * WorldMsg: World
         */
    }
}
