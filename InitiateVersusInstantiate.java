public class InitiateVersusInstantiate {
    /**
     * You have to INITIATE a class to invoke method:
     * Classname.methodName();
     * 
     * Example: ClassTest.methodTest();
     * 
     * 
     * You have to INSTANTIATE a class OBJECT to invoke method:
     * Classname objectName = new Classname();
     * objectName.methodName();
     * 
     * Example: ClassTest objectTest = new ClassTest
     * objectTest.methodTest();
     */
}
